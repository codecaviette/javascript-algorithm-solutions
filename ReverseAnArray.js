/**********************************
Code Challenge: 
    Reverse an array

Ex:  
    var array = ["h","e","l","l","o"];
*/


/**********************************
Solution:
*/

var array = ["h","e","l","l","o"];
var reverseArray = (array) => {          
    return array.reverse();
};

console.log(reverseArray(array));
//output: ["o","l","l","e","h"]
